/**
 * Creates a minimal valid PNG (1×1 transparent pixel).
 */
function createMinimalPng(): Buffer {
  // PNG signature + IHDR + IDAT + IEND for a 1×1 RGBA image
  return Buffer.from([
    0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG signature
    0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, // IHDR length + type
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // width=1, height=1
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, // bit depth=8, color=RGB, CRC
    0xde, 0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, // IDAT length + type
    0x54, 0x08, 0xd7, 0x63, 0xf8, 0xcf, 0xc0, 0x00, // compressed pixel data
    0x00, 0x00, 0x02, 0x00, 0x01, 0xe2, 0x21, 0xbc, // CRC
    0x33, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, // IEND length + type
    0x44, 0xae, 0x42, 0x60, 0x82,                   // IEND CRC
  ]);
}

/**
 * Creates a minimal valid WebP (1×1 pixel).
 * Using the simplest VP8L lossless encoding.
 */
function createMinimalWebp(): Buffer {
  // Minimal WebP RIFF container (VP8L, lossless, 1x1)
  return Buffer.from([
    0x52, 0x49, 0x46, 0x46, // "RIFF"
    0x24, 0x00, 0x00, 0x00, // file size (36 bytes)
    0x57, 0x45, 0x42, 0x50, // "WEBP"
    0x56, 0x50, 0x38, 0x4c, // "VP8L"
    0x14, 0x00, 0x00, 0x00, // chunk size
    0x2f, 0x00, 0x00, 0x00, // signature 0x2f
    0x00, 0x00, 0x00, 0xfe, // width=1, height=1 encoded
    0x07, 0x22, 0x00, 0x00, // image data
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
  ]);
}

/**
 * Creates a fake oversized JPEG (valid header + padding to exceed 10 MB).
 */
function createOversizedJpeg(): Buffer {
  const header = createMinimalJpeg();
  // Pad to 10.5 MB to reliably exceed the 10 MB limit
  const TARGET_SIZE = 10.5 * 1024 * 1024;
  const padding = Buffer.alloc(TARGET_SIZE - header.length, 0x00);
  return Buffer.concat([header, padding]);
}

/**
 * Creates a minimal GIF87a file (unsupported format).
 */
function createGif(): Buffer {
  return Buffer.from([
    0x47, 0x49, 0x46, 0x38, 0x37, 0x61, // "GIF87a"
    0x01, 0x00, 0x01, 0x00, 0x80, 0x00, // 1×1, global color table flag
    0x00, 0xff, 0xff, 0xff, 0x00, 0x00, // background + 2 colours
    0x00, 0x2c, 0x00, 0x00, 0x00, 0x00, // image descriptor
    0x01, 0x00, 0x01, 0x00, 0x00, 0x02,
    0x02, 0x4c, 0x01, 0x00, 0x3b,       // LZW data + trailer
  ]);
}

// Generate all fixture files
const files: Array<{ name: string; data: Buffer }> = [
  { name: 'unused-device.jpg',   data: createMinimalJpeg('unused-device') },
  { name: 'cracked-screen.png',  data: createMinimalPng() },
  { name: 'blurry-photo.webp',   data: createMinimalWebp() },
  { name: 'oversized-image.jpg', data: createOversizedJpeg() },
  { name: 'wrong-type.gif',      data: createGif() },
];

for (const file of files) {
  const outPath = path.join(FIXTURES_DIR, file.name);
  if (!fs.existsSync(outPath)) {
    fs.writeFileSync(outPath, file.data);
    console.log(`Created: ${file.name} (${(file.data.length / 1024).toFixed(1)} KB)`);
  } else {
    console.log(`Exists:  ${file.name} (${(fs.statSync(outPath).size / 1024).toFixed(1)} KB)`);
  }
}

console.log('Fixture generation complete.');
